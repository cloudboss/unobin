package codegen

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
	"strconv"
	"text/template"

	"github.com/cloudboss/unobin/pkg/lang"
)

// GenerateUBModule produces the Go source for a UB module's
// generated package. The package's name is alias; it exports a
// `Module()` function returning a `*runtime.Module` with one
// `*runtime.CompositeType` per entry in the manifest's `exports:`
// block. bodies must hold a parsed body file under each export name.
func GenerateUBModule(alias string, manifest *lang.File, bodies map[string]*lang.File) ([]byte, error) {
	if alias == "" {
		return nil, fmt.Errorf("ubmodule: alias is required")
	}
	if manifest == nil || manifest.Body == nil {
		return nil, fmt.Errorf("ubmodule: manifest is required")
	}
	description := manifestDescription(manifest)
	exports, err := manifestExports(manifest)
	if err != nil {
		return nil, err
	}
	composites := make([]compositeEntry, 0, len(exports))
	for _, name := range exports {
		body, ok := bodies[name]
		if !ok {
			return nil, fmt.Errorf("ubmodule %q: export %q has no body", alias, name)
		}
		encoded, err := EncodeNode(body)
		if err != nil {
			return nil, fmt.Errorf("ubmodule %q: encode %q body: %w", alias, name, err)
		}
		composites = append(composites, compositeEntry{Name: name, Body: encoded})
	}

	var buf bytes.Buffer
	data := struct {
		Alias       string
		Description string
		Composites  []compositeEntry
	}{
		Alias:       alias,
		Description: description,
		Composites:  composites,
	}
	if err := ubModuleTemplate.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("ubmodule: %w", err)
	}
	out, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("ubmodule: format generated source: %w", err)
	}
	return out, nil
}

type compositeEntry struct {
	Name string
	Body string
}

func manifestDescription(f *lang.File) string {
	for _, fld := range f.Body.Fields {
		if fld.Key.Kind != lang.FieldIdent || fld.Key.Name != "description" {
			continue
		}
		if s, ok := fld.Value.(*lang.StringLit); ok {
			return s.Value
		}
	}
	return ""
}

func manifestExports(f *lang.File) ([]string, error) {
	for _, fld := range f.Body.Fields {
		if fld.Key.Kind != lang.FieldIdent || fld.Key.Name != "exports" {
			continue
		}
		obj, ok := fld.Value.(*lang.ObjectLit)
		if !ok {
			return nil, fmt.Errorf("ubmodule: `exports:` must be an object")
		}
		names := make([]string, 0, len(obj.Fields))
		for _, ef := range obj.Fields {
			if ef.Key.Kind != lang.FieldIdent || ef.Key.IsMeta() {
				continue
			}
			names = append(names, ef.Key.Name)
		}
		sort.Strings(names)
		return names, nil
	}
	return nil, nil
}

var ubModuleTemplate = template.Must(template.New("ubmodule.go").Funcs(template.FuncMap{
	"quote": strconv.Quote,
}).Parse(`// Code generated by unobin. DO NOT EDIT.
package {{.Alias}}

import (
	"github.com/cloudboss/unobin/pkg/lang"
	"github.com/cloudboss/unobin/pkg/runtime"
)

func Module() *runtime.Module {
	return &runtime.Module{
		Name:        {{quote .Alias}},
		Description: {{quote .Description}},
		Composites: map[string]*runtime.CompositeType{
{{range .Composites}}			{{quote .Name}}: {Name: {{quote .Name}}, Body: {{.Body}}},
{{end}}		},
	}
}
`))
