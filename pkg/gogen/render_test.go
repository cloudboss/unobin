func sampleResourceSchema() ResourceSchema {
	return ResourceSchema{
		GoName:      "S3Bucket",
		CloudType:   "AWS::S3::Bucket",
		Description: "An S3 bucket for storing objects",
		InputFields: []Field{
			{Name: "BucketName", GoType: "string", Description: "The bucket name", Required: true},
			{Name: "Tags", GoType: "map[string]string", Description: "Resource tags", Required: false},
		},
		OutputFields: []Field{
			{Name: "Arn", GoType: "string", Description: "The ARN of the bucket"},
			{Name: "DomainName", GoType: "string", Description: "The domain name"},
		},
		CreateOnlyFields:  []string{"BucketName"},
		PrimaryIdentifier: []string{"BucketName"},
	}
}

func TestResourceFileProducesParseableGo(t *testing.T) {
	src, err := ResourceFile(sampleResourceSchema(), "tf")
	if err != nil {
		t.Fatalf("ResourceFile: %v", err)
	}

	fset := token.NewFileSet()
	_, err = parser.ParseFile(fset, "s3_bucket.go", src, parser.AllErrors)
	if err != nil {
		t.Fatalf("generated source does not parse: %v\n\nSource:\n%s", err, src)
	}

	s := string(src)
	checks := []string{
		"Generated by unobin generate gomodule --from tf",
		"package resources",
		"type S3Bucket struct {",
		"type S3BucketOutput struct {",
		"BucketName string `ub:\"bucket-name\"`",
		"Tags map[string]string `ub:\"tags\"`",
		"Arn string `ub:\"arn\"`",
		"func (r *S3Bucket) SchemaVersion() int { return 1 }",
		"func (r *S3Bucket) Create(ctx context.Context, cfg any) (*S3BucketOutput, error)",
		"func (r *S3Bucket) Read(ctx context.Context, cfg any, priorOutputs *S3BucketOutput) (*S3BucketOutput, error)",
		"func (r *S3Bucket) Update(ctx context.Context, cfg any, prior runtime.Prior[S3Bucket, *S3BucketOutput]) (*S3BucketOutput, error)",
		`"github.com/cloudboss/unobin/pkg/runtime"`,
		"func (r *S3Bucket) Delete(ctx context.Context, cfg any, priorOutputs *S3BucketOutput) error",
		"return []string{\n\t\t\"bucket-name\",\n\t}",
		`panic("not implemented")`,
	}
	for _, c := range checks {
		if !strings.Contains(s, c) {
			t.Errorf("expected generated source to contain %q", c)
		}
	}
}

