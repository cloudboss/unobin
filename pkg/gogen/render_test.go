func TestLibraryFileProducesParseableGo(t *testing.T) {
	resources := []ResourceSchema{sampleResourceSchema()}

	src, err := LibraryFile("aws", resources, nil, nil, "example.com/aws", "tf")
	if err != nil {
		t.Fatalf("LibraryFile: %v", err)
	}

	fset := token.NewFileSet()
	_, err = parser.ParseFile(fset, "library.go", src, parser.AllErrors)
	if err != nil {
		t.Fatalf("generated source does not parse: %v\n\nSource:\n%s", err, src)
	}

	s := string(src)
	checks := []string{
		"Generated by unobin generate gomodule --from tf",
		"package aws",
		`"example.com/aws/resources"`,
		"func Library() *runtime.Library",
		"Resources: map[string]runtime.ResourceRegistration",
		`"s3-bucket": runtime.MakeResource[resources.S3Bucket, *resources.S3BucketOutput]()`,
	}
	for _, c := range checks {
		if !strings.Contains(s, c) {
			t.Errorf("expected generated source to contain %q", c)
		}
	}
	if strings.Contains(s, "DataSources") {
		t.Error("expected no DataSources section")
	}
	if strings.Contains(s, "/data") {
		t.Error("expected no /data import when no data sources")
	}
}

